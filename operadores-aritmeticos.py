# Operadores  /  Operação
#   +            Adição
#   -            Subtração
#   *            Multiplicação
#   /            Divisão Real (float)
#   //           Divisão Inteira (int)
#   %            Módulo/MOD (resto da divisão inteira)
#   **           Potenciação


a=10
b=5

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

